<?php
declare(strict_types=1);

namespace App\Module\User\Domain\Model\User;

final class User
{
    private $id;

    private $name;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId($id): void
    {
        $this->id = $id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }
}